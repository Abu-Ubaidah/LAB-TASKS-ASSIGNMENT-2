import java.util.*;
public class Task8Calculate {
    
        public static void main(String[] args) {
            Scanner sc=new Scanner(System.in);
            int num1,num2;
            System.out.println("Enter Number 1");
            num1=sc.nextInt();
            System.out.println("Enter Number 2");
            num2=sc.nextInt();
            System.out.println(num1+"+"+num2+"="+(num1+num2));
            System.out.println(num1+"-"+num2+"="+(num1-num2));
            System.out.println(num1+"*"+num2+"="+(num1*num2));
            System.out.println(num1+"/"+num2+"="+(num1/num2));
        }
    }


